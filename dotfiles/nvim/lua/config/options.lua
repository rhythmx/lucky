local opt = vim.opt

opt.expandtab = true   -- use spaces instead of tab characters
opt.tabstop = 4        -- a tab character displays as 4 spaces wide
opt.softtabstop = 4    -- pressing tab/backspace acts on 4 spaces
opt.shiftwidth = 4     -- auto-indent (>>, <<, ==) uses 4 spaces
opt.smartindent = true -- auto-indent new lines based on syntax

